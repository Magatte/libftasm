section .text
	global _ft_isdigit

_ft_isdigit:
	cmp		rdi, '0'
	jb		end0
	cmp		rdi, '9'
	ja		end0
	jmp 	end1

end0:
	mov		al, 0
	ret

end1:
	mov		al, 1
	ret
